		<h2>About Us</h2>
				<p> The Maskara Theatre Ensemble was conceived in La Salle College when it was founded by Peque Gallaga in 1972. 
				The organization which used to be called “Crossing Kamatis Rapapap Kutsi-Kutsi Society” then changed to “The La Salle Theatre Guild” was created to enhance the cultural development among its members. In 1973, the group bore the name “Maskara Theatre Ensemble”,
				the first word which had been derived from the a logo representing the Igorot symbol for fetility, designed by graphic 
				designer Franco Patriarca.Maskara had become an outlet for people who love to do theatre work and who believe in the magic of theatre.
				It serves to cultivate the foundation of theatre in entertainment, education and advocacy through the extensive and rigorous training 
				it provides its members; and the varied productions it represents to the community. 
